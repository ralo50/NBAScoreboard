package com.ralo.nbascoreboard.Utils.DataClasses;

public class GameStat {

    private String awayTeamStat;
    private String statDescription;
    private String homeTeamStat;

    public String getAwayTeamStat() {
        return awayTeamStat;
    }

    public void setAwayTeamStat(String awayTeamStat) {
        this.awayTeamStat = awayTeamStat;
    }

    public String getStatDescription() {
        return statDescription;
    }

    public void setStatDescription(String statDescription) {
        this.statDescription = statDescription;
    }

    public String getHomeTeamStat() {
        return homeTeamStat;
    }

    public void setHomeTeamStat(String homeTeamStat) {
        this.homeTeamStat = homeTeamStat;
    }
}
